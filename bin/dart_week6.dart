void main(){
  int x = 10;
  double y = 5;
  print("Arithemetic x + y : $x + $y = ${x + y}");//1.Addition
  print("Arithemetic x - y : $x - $y = ${x - y}");//2.Subtraction
  print("Arithemetic x * y : $x * $y = ${x * y}");//3.Multi
  print("Arithemetic x / y : $x / $y = ${x / y}");//4.Division 
  print("Arithemetic x ~/y : $x ~/$y = ${x ~/y}");//5.Modulus
  print("Arithemetic x % y : $x % $y = ${x % y}");//6.Modulus
}